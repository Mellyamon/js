//Data Types in Javascript
// 1. String: Represents text. Example: "Hello, World"
let email = "amon@gmail.com";
// 2. Number: Represents both interger and floating-point numbers. Example: 42, 3.14
let score = 65.8;
// 3. Boolean: Represents true or false values. Example: true, false
let isLoggedIn = false;
// 4. Object: Represnts complex data structures. Example: {name: 'Amon', age: 30}
let user = { name: "Alice", age: 30 };
// 5. Array/List: Represents a list of values. Example: [1,2,3,4,5]
let fruits = [
  "apple",
  "banana",
  "cherry",
  90,
  [1, 2, 3],
  ["matatu", "bodaboda"],
];
// 6. Undefined: Represents a variable that has been declared but not assigned a value Example: let x;
let x;
// 7. Null: Represents the intentional absence of any object value. Example: let y = null;
let age = null;
// 8. Symbol: Represents a unique identifier. Example: let sym = Symbol("id");
